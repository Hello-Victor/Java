    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
       
        String fName ="";
        String lName = "";
        String dBirth="";
        long pNumber = 0;
        int age = 0;
        double height=0;
        double weidth=0;
        long ssNumber = 0;
      try{
        fName = txtFN.getText();
        lName = txtLN.getText();
        pNumber = Long.parseLong(txtPN.getText());
        dBirth = txtDOB.getText();
        age = Integer.parseInt(txtAge.getText());
        height = Double.parseDouble(txtHeight.getText());
        weidth = Double.parseDouble(txtWeight.getText());
        ssNumber = Long.parseLong(txtSSN.getText());
      }catch(Exception e){
          JOptionPane.showMessageDialog(this,"Please check the input format","",JOptionPane.WARNING_MESSAGE);
      }
        
        
        CPi cpi = cpiDao.addCPi();
        
        cpi.setfName(fName);
        cpi.setlName(lName);
        cpi.setpNumber(pNumber);
        cpi.setdBirth(dBirth);
        cpi.setAge(age);
        cpi.setHeight(height);
        cpi.setWeidth(weidth);
        cpi.setSsNumber(ssNumber);
        
        JOptionPane.showMessageDialog(this,"Add Succeed!");
    }//GEN-LAST:event_btnSaveActionPerformed


