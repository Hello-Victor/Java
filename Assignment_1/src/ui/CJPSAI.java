    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String bName="";
        int BRNumber=0;
        int ANumber=0;
        double balance=0;
        String AType="";
       
      try{
        bName = txtAB.getText();
        BRNumber= Integer.parseInt(txtAP.getText());
        ANumber = Integer.parseInt(txtBAN.getText());
        balance = Double.parseDouble(txtBN.getText());
        AType = txtBRN.getText();
      }catch(Exception e){
           JOptionPane.showMessageDialog(this,"Please check the input format","",JOptionPane.WARNING_MESSAGE);
      }
        
   
        Sai sai = saiDao.addSai();
    
        sai.setbName(bName);
        sai.setBRNumber(BRNumber);
        sai.setANumber(ANumber);
        sai.setBalance(balance);
        sai.setAType(AType);
  
         JOptionPane.showMessageDialog(this,"Add Succeed!");
         
      
         txtAB.setText("");
         txtAP.setText("");
         txtBAN.setText("");
         txtBN.setText("");
         txtBRN.setText("");
    }//GEN-LAST:event_btnSaveActionPerformed


