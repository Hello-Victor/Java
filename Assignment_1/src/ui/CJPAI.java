    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String sa="";
        String city = "";
        String sate = "";
        int zip = 0;
      try{
        sa = txtSA.getText();
        city = txtCity.getText();
        sate = txtState.getText();
        zip = Integer.parseInt(txtZip.getText());
      }catch(Exception e){
           JOptionPane.showMessageDialog(this,"Please check the input format","",JOptionPane.WARNING_MESSAGE);
      }
        
       Address address = addressDaoImp.addAddress();
       
       address.setCity(city);
       address.setStreet(sa);
       address.setState(sate);
       address.setId(zip);
       
        JOptionPane.showMessageDialog(this,"Add Succeed!");
       

       txtSA.setText("");
       txtCity.setText("");
       txtState.setText("");
       txtZip.setText("");
       
    }//GEN-LAST:event_jButton1ActionPerformed

