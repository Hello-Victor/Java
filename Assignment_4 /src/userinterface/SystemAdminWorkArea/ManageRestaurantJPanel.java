     @Override
    public void refreshTable() {
        List<Object[]> rows=new ArrayList<>();
        List<Restaurant> restaurants = system.getRestaurantDirectory().getRestaurants();
        for(Restaurant restaurant:restaurants){
            ArrayList<UserAccount> userAccountList = restaurant.getUserAccountDirectory().getUserAccountList();
            for(UserAccount userAccount:userAccountList){
                Object[] row = new Object[3];
                    row[0] = restaurant;
                    row[1] = userAccount.getUsername();
                    row[2] = userAccount.getPassword();
                    rows.add(row);
            }
        }
        dorefreshTable(jTable1, rows);
    }

