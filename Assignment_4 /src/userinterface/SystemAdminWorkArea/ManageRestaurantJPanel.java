    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String restaurantName=jTextField1.getText();
        String userName=jTextField2.getText();
        String password=jTextField3.getText();
        Employee employee = new Employee(userName);
        AdminRole adminRole=new AdminRole();
        Restaurant newRestaurant=new Restaurant(restaurantName);
        
        newRestaurant.getUserAccountDirectory().createUserAccount(userName, password, employee, adminRole);
        
        system.getRestaurantDirectory().getRestaurants().add(newRestaurant);
        refreshTable();
    }//GEN-LAST:event_jButton3ActionPerformed

