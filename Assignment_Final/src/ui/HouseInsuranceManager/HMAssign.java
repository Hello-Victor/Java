    public void populateCombobox() {
        jComboBox2.removeAllItems();
        assignees = new ArrayList<Employee>();
        for (Employee e : houseInsuranceOrganization.getEmployeeDirectory().getavailableIterator()) {
            jComboBox2.addItem(e.getId() + " - " + e.getName());
            assignees.add(e);
        }
    }

    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        Object[] columns = {"Time", "Name", "Location", "Loss Amount", "Status", "Feedback"};
        model.setRowCount(0);
        model.setColumnIdentifiers(columns);
        for (WorkRequest r : houseInsuranceOrganization.getWorkQueue().getIterator()) {
            HouseInsuranceRequest request = (HouseInsuranceRequest) r;
            Object[] row = new Object[6];
            row[0] = request;
            row[1] = request.getSender().getEmployee().getName();
            row[2] = request.getLocation();
            row[3] = request.getLossString();
            row[4] = request.getStatusString();
            row[5] = request.getFeedbackString();
            model.addRow(row);
        }
    }

