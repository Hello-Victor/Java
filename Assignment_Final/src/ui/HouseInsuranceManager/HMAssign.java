    private void jPanel4MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel4MousePressed
        HouseInsuranceRequest request;
        try {
            request = (HouseInsuranceRequest) chooseObjectFromTable(jTable1, 0, "a request");
        } catch (Exception ex) {
            return;
        }
        if (request.getStatus() != WorkRequest.Status.Ordered) {
            warn("Cannot cancel the request at the current status!");
            return;
        }
        request.cancel();
        refresh();
        info("Request Canceled");
    }//GEN-LAST:event_jPanel4MousePressed


