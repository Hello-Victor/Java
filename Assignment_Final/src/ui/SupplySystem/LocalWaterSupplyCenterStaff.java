    public void done(String decision) {
        WaterRequest request;
        try {
            request = (WaterRequest) chooseObjectFromTable(tblRequests, 0, "a request");
        } catch (Exception ex) {
            return;
        }
        request.setDecision(decision);
        request.done();
        refresh();
    }
