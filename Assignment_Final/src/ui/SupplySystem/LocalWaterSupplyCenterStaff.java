    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblRequests.getModel();
        model.setRowCount(0);
        for (WorkRequest r : userAccount.getEmployee().getWorkQueue().getIterator()) {
            WaterRequest p = (WaterRequest) r;
            Object row[] = new Object[6];
            row[0] = p;
            row[1] = p.getStatusString();
            row[2] = p.getDecision();
            row[3] = p.getLocation();
            row[4] = p.getMessage();
            row[5] = p.getFeedbackString();
            model.addRow(row);
        }
    }

