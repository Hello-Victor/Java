    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        UserAccount assignee;
        Integer foodRemain;
        try {
            assignee = (UserAccount) chooseObjectFromTable(tblUsers, 1, "a staff");
        } catch (Exception ex) {
            return;
        }
        if (address.getAddress() == null) {
            warn("Please set the location first!");
            return;
        }
        try {
            foodRemain = Integer.parseInt(txtFoodAmount.getText());
        } catch (Exception e) {
            warn("Wrong format of food distribution!");
            return;
        }
        FoodRequest workRequest = new FoodRequest();
        ArrayList<EachFoodRecord> foodRecord = new ArrayList<>();
        workRequest.setFoodRecord(foodRecord);
        workRequest.setReceiver(foodOrganization);
        workRequest.setLocation(address.getAddress());
        workRequest.setAssignee(assignee);
        workRequest.setFoodRemain(foodRemain);
        info("Created Successfully.");
        backAction();
    }//GEN-LAST:event_jButton9ActionPerformed

