    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblUsers.getModel();
        model.setRowCount(0);
        for (Employee p : foodOrganization.getEmployeeDirectory().getavailableIterator()) {
            Object row[] = new Object[4];
            row[0] = p.getName();
            row[1] = p.getUserAccount();
            row[2] = p.isIsAvailable();
            model.addRow(row);
        }
    }

