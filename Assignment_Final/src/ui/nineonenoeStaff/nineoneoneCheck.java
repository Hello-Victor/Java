    public void populateTable1() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        Object[] columns = {"Time", "Name", "Location", "Status", "Feedback"};
        model.setRowCount(0);
        model.setColumnIdentifiers(columns);
        for (int i = employee.getWorkQueue().getWorkRequestList().size() - 1; i >= 0; i--) {
            WorkRequest request = employee.getWorkQueue().getWorkRequestList().get(i);
            Object[] row = new Object[5];
            row[0] = request;
            row[1] = request.getSender().getEmployee().getName();
            row[2] = request.getLocation();
            row[3] = request.getStatusString();
            row[4] = request.getFeedbackString();
            model.addRow(row);
        }
    }

    public void populateTable2() {
        DefaultTableModel model = (DefaultTableModel) jTable3.getModel();
        Object[] columns = {"Time", "Name", "Location", "Status", "Feedback"};
        model.setRowCount(0);
        model.setColumnIdentifiers(columns);
        for (WorkRequest request : rescueOrganization.getWorkQueue().getIterator()) {
            RescueRequest request1 = (RescueRequest) request;
            if (request1.getHospitalRequest() != null) {
                Object[] row = new Object[5];
                row[0] = request;
                row[1] = request.getSender().getEmployee().getName();
                row[2] = request.getLocation();
                row[3] = request.getStatusString();
                row[4] = request.getFeedbackString();
                model.addRow(row);
            }
        }

    }

