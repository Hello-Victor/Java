    private void jPanel2MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel2MousePressed
        RescueRequest request;
        try {
            request = (RescueRequest) chooseObjectFromTable(jTable1, 0, "a work request");
        } catch (Exception ex) {
            return;
        }
        if (!request.isDone()) {
            warn("Unfinished request is not allowed to be sent to hospital!");
            return;
        }
        HospitalRequest hospitalRequest = new HospitalRequest();
        hospitalRequest.setStatus(WorkRequest.Status.Ordered);
        hospitalRequest.setSender(request.getSender());
        hospitalRequest.setReceiver(hospitalOrganization);
        hospitalRequest.setLocation(request.getLocation());
        request.setHospitalRequest(hospitalRequest);
        hospitalRequest.setMessage(request.getMessage());
        info("Cure sent");
        refresh();
    }//GEN-LAST:event_jPanel2MousePressed

