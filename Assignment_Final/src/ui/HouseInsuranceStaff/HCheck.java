    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        Object[] columns = {"Time", "Name", "Location", "Loss Amount", "Status", "Feedback"};
        model.setRowCount(0);
        model.setColumnIdentifiers(columns);
        for (int i = employee.getWorkQueue().getWorkRequestList().size() - 1; i >= 0; i--) {
            HouseInsuranceRequest request = (HouseInsuranceRequest) employee.getWorkQueue().getWorkRequestList().get(i);
            Object[] row = new Object[6];
            row[0] = request;
            row[1] = request.getSender().getEmployee().getName();
            row[2] = request.getLocation();
            row[3] = request.getLossString();
            row[4] = request.getStatusString();
            row[5] = request.getFeedbackString();
            model.addRow(row);
        }
    }

