    private void jPanel2MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel2MousePressed

        HouseInsuranceRequest request;
        try {
            request = (HouseInsuranceRequest) chooseObjectFromTable(jTable1, 0, "a work request");
        } catch (Exception ex) {
            return;
        }
        if (request.getStatus() != WorkRequest.Status.Accepted) {
            warn("Cannot Finish the reuquest at current status!");
            return;
        }
        String loss = inputbox("Type your confirmed loss amount:");
        int lossInt;
        try {
            lossInt = Integer.parseInt(loss);
        } catch (Exception ex) {
            warn("Wrong format of loss amount!");
            return;
        }
        request.setLoss(lossInt);
        request.done();
        refresh();
        info("Request Finished!");
    }//GEN-LAST:event_jPanel2MousePressed

