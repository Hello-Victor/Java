    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        Object[] columns = {"Time", "Type", "Status", "Feedback"};
        model.setRowCount(0);
        model.setColumnIdentifiers(columns);
        for (int i = employee.getWorkQueue().getWorkRequestList().size() - 1; i >= 0; i--) {
            WorkRequest request = employee.getWorkQueue().getWorkRequestList().get(i);
            Object[] row = new Object[5];
            row[0] = request;
            row[1] = WorkRequest.getType(request);
            row[2] = request.getStatusString();
            row[3] = request.getFeedbackString();
            model.addRow(row);
        }
    }

