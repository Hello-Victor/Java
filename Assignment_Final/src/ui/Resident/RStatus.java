    private void jPanel1MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel1MousePressed
        WorkRequest request;
        try {
            request = (WorkRequest) chooseObjectFromTable(jTable1, 0, "a work request");
        } catch (Exception ex) {
            return;
        }
        if (request.getStatus() != WorkRequest.Status.Completed) {
            warn("Writing feedback on unfinished request is not allowed!");
            return;
        }
        String feedback = inputbox("Type your feedback here please:");
        request.setFeedback(feedback);
        refresh();
    }//GEN-LAST:event_jPanel1MousePressed

