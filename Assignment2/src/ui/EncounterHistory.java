    public void reflashTable(){
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        String community = jComboBox1.getSelectedItem().toString();
        List<EncounterModel> encounterModelList =   encounterDirectory.getEncounterModelListByCommunity(community);
            for (EncounterModel e : encounterModelList) {
            
                Object row[] = new Object[6];
                row[0] = e.getPerson().getName();
                row[1] = e.getPulse();
                row[2] = e.getBloodPressure();
                row[3] = e.getTemperature();
                row[4] = e.getSpO2();
                row[5] = e.getTimeNow();
                model.addRow(row);
        }
        int encounterModelCountByCommunity = encounterDirectory.getEncounterModelCountByCommunity(community);
        jTextField1.setText(encounterModelCountByCommunity + "");
    }
    
    
    
