    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            CourseOffer sa = (CourseOffer) chooseObjectFromTable(jTable1, 2, "a course");
            if (sa.numStudent() > 0) {
                JOptionPane.showMessageDialog(this, "Wrong! Course has at least one student enrolled!");
                return;
            }
            sa.getCourseSchedule().removeCourseOffer(sa);
            facultyProfile.getFacultyassignments().remove(sa.getFacultyassignment());
            refreshTable();
        } catch (Exception ex) {
        }

    }//GEN-LAST:event_jButton2ActionPerformed

