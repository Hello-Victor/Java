    @Override
    public void refreshTable() {

        DefaultTableModel model = (DefaultTableModel) this.jTable1.getModel();
        model.setRowCount(0);
        for (FacultyAssignment fa : facultyProfile.getFacultyassignments()) {
            CourseOffer co = fa.getCourseoffer();
            model.addRow(new Object[]{co.getCourseSchedule().getSemester(), co.getCourse().getName(), co, co.getCourse().getCredits(),
                Double.isNaN(co.getGrade()) ? "N/A" : String.format("%.2f", co.getGrade()), co.numStudent(),
                Double.isNaN(co.getScore()) ? "N/A" : String.format("%.2f", co.getScore())});
        }

        this.jTable1.setRowHeight(30);
        this.jTable1.setModel(model);
        jTextField2.setText(String.format("%.2f", facultyProfile.getScore()));
    }

