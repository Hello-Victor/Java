    public void refreshTable() {
        University university = University.getInstance();

        DefaultTableModel model = (DefaultTableModel) this.jTable1.getModel();
        model.setRowCount(0);

        ArrayList<Company> companies = new ArrayList<Company>();
        for (Company company : companyDirectory.getCompanyMap().values()) {
            companies.add(company);
        }
        Collections.sort(companies, Comparator.comparing(Company::getWeight).reversed());
        for (Company company : companies) {
            model.addRow(new Object[]{company, String.format("%.2f", company.getGPA()), "$" + company.getAverageSalary() / 1000 + "k", String.format("%.2f", company.getAveragePromotionTimes()), String.format("%.2f", company.getWeight())});
        }

        this.jTable1.setRowHeight(30);
        this.jTable1.setModel(model);
    }

