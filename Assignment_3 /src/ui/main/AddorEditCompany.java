    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String companyName = this.jTextField1.getText();
        String userName = this.jTextField2.getText();
        String password = this.jTextField3.getText();
        try {
            if (company == null) {
                company = companyDirectory.newCompany(companyName, "", "", userName, password);
            } else {
                companyDirectory.modifyCompany(company, companyName, "", "", userName, password);
            }
            backAction();
        } catch (CompanyDirectory.CompanyNameConflictException ex) {
            JOptionPane.showMessageDialog(mainWorkArea, "Company Name Conflict");
        } catch (AccountDirectory.AccountNameConflictException ex) {
            JOptionPane.showMessageDialog(mainWorkArea, "Account Name Conflict");
        }
    }//GEN-LAST:event_jButton2ActionPerformed


