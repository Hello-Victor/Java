    @Override
    public void refreshTable() {
        DefaultTableModel model = (DefaultTableModel) this.jTable1.getModel();
        model.setRowCount(0);
        ArrayList<College> colleges = new ArrayList<College>();
        HashMap<College, Double[]> collegeMap = new HashMap<College, Double[]>();
        for (College c : university.getColleges().values()) {
            Account account = c.getAccount();
            Double ap = 0.;
            Double jp = 0.;
            int sn = 0;
            Double fp = 0.;
            int fn = 0;
            for (Department department : c.getDepartments().values()) {
                for (StudentProfile sp : department.getStudentdirectory().getStudentMap().values()) {
                    if (sp.isGraduated()) {
                        ap += sp.getAcademicWeight();
                        jp += sp.getIndustialWeight();
                        sn++;
                    }
                }
                for (FacultyProfile fpp : department.getFacultydirectory().getTeacherMap().values()) {
                    fp += fpp.getScore();
                    fn++;
                }
            }
            Double[] data = {ap / sn, jp / sn, fp / fn};
            collegeMap.put(c, data);
            colleges.add(c);
        }
        Collections.sort(colleges, Comparator.comparing((College c) -> collegeMap.get(c)[2]).reversed());
        for (College c : colleges) {
            model.addRow(new Object[]{c.getName(), String.format("%.2f", collegeMap.get(c)[0]), String.format("%.2f", collegeMap.get(c)[1]), String.format("%.2f", collegeMap.get(c)[2])});
        }
        this.jTable1.setRowHeight(30);
        this.jTable1.setModel(model);
    }

