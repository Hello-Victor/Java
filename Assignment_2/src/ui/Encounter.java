    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
         Person p = (Person) jComboBox1.getSelectedItem();
         String dateNow = jTextField1.getText();
         String pluse = jTextField2.getText();
         String blood = jTextField3.getText();
         String temp = jTextField4.getText();
         String spO2 = jTextField5.getText();
         
         if (p == null) {
             JOptionPane.showMessageDialog(this,"please select person");
             return;
         }else if ("".equals(dateNow)) {
             JOptionPane.showMessageDialog(this,"date is  empty");
             return;
         }else if ("".equals(pluse)) {
             JOptionPane.showMessageDialog(this,"pluse is  empty");
             return;
         }else if ("".equals(blood)) {
             JOptionPane.showMessageDialog(this,"Blood Pressure  is  empty");
             return;
         } else if ("".equals(temp)) {
             JOptionPane.showMessageDialog(this,"Temperature  is  empty");
             return;
         } else if ("".equals(spO2)) {
             JOptionPane.showMessageDialog(this,"spO2  is  empty");
             return;
         } 
         EncounterModel e = new EncounterModel(p,dateNow,pluse,blood,temp,spO2);
         this.encounterDirectory.addEncounter(e);
          JOptionPane.showMessageDialog(this,"create Encounte success");
    }//GEN-LAST:event_jButton2ActionPerformed


